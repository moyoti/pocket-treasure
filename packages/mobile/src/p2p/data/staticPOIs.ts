export function getStaticPOIsNearby(latitude: number, longitude: number, radiusKm: number): POI[] {
  console.log(`[StaticPOI] Searching near ${latitude}, ${longitude} within ${radiusKm}km`);
  const results: POI[] = [];
  const R = 6371;

  for (const poi of STATIC_POI_DEFINITIONS) {
    const dLat = ((poi.latitude - latitude) * Math.PI) / 180;
    const dLon = ((poi.longitude - longitude) * Math.PI) / 180;
    const a =
      Math.sin(dLat / 2) * Math.sin(dLat / 2) +
      Math.cos((latitude * Math.PI) / 180) *
        Math.cos((poi.latitude * Math.PI) / 180) *
        Math.sin(dLon / 2) *
        Math.sin(dLon / 2);
    const c = 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
    const distance = R * c;

    if (distance <= radiusKm) {
      console.log(`[StaticPOI] Found: ${poi.name} at ${distance.toFixed(2)}km`);
      results.push(poi);
    }
  }

  console.log(`[StaticPOI] Total found: ${results.length}`);
  return results;
}